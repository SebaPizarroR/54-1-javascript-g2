//MAYOR QUE >
console.log("5 > 3: ", 5 > 3); //true

//MENOR QUE <
console.log("5 < 3: ", 5 < 3); // false

//MAYOR O IGUAL QUE
console.log("5 >= 5:", 5 >= 5); // true

//MENOR O IGUAL QUE
console.log("5 <= 3:", 5 <= 3); // false

//IGUAL QUE
console.log("5 == 3:", 5 == 3); // false

//IGUAL QUE
console.log("5 == 5:", 5 == 5); // true

//IGUAL QUE
console.log("5 == '5':", 5 == '5'); // true

//ESTRICTAMENTE IGUAL QUE
//VALIDA QUE EL VALOR Y EL TIPO DE DATO SEAN IGUAL
console.log("5 === '5':", 5 === '5'); // false

//DISTINTO DE !=
console.log("4 != 5:", 4 != 5); // true